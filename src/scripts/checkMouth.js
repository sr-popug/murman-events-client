export default function checkMouth(mouth) {
	if (mouth === 'Yan') {
		return 'Января'
	}
	if (mouth === 'Feb') {
		return 'Февраля'
	}
	if (mouth === 'Mar') {
		return 'Марта'
	}
	if (mouth === 'Apr') {
		return 'Апреля'
	}
	if (mouth === 'May') {
		return 'Мая'
	}
	if (mouth === 'June') {
		return 'Июня'
	}
	if (mouth === 'July') {
		return 'Июля'
	}
	if (mouth === 'Aug') {
		return 'Августа'
	}
	if (mouth === 'Sep') {
		return 'Сентября'
	}
	if (mouth === 'Oct') {
		return 'Октября'
	}
	if (mouth === 'Nov') {
		return 'Ноября'
	}
	if (mouth === 'Dec') {
		return 'Декабря'
	}
}
